# -------0-----------1------------2----3----4--
lista = [18, 'Rafael Oliveira', True, [], 1.85]
print(lista) # [18, 'Rafael Oliveira', True, [], 1.85]
print(lista[2]) # Rafael Oliveira

lista[2] = 'Rafa'
print(lista[2]) # Rafa
print(lista) # [18, 'Rafa', True, [], 1.85]